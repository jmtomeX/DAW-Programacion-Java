public class PruebasReturn{
   public static void main(String[] args){
      System.out.println("Math.round(4.6) >> " + Math.round(4.6)); 	
   
      System.out.println("Math.round(6.3) + Math.abs(-34.5) >> " + Math.round(6.3) + Math.abs(-34.5));	
   
      System.out.println("Math.pow(4, 2) >> " +Math.pow(4, 2));
   
      System.out.println("Math.pow(3, 3) + Math.sqrt(16) >> Resultado = " + Math.pow(3, 3) + Math.sqrt(16));
   
      System.out.println("Math.max(8.7, 10.5) >>" + Math.max(8.7, 10.5));	
   
      System.out.println("Math.abs(Math.min(19, 4) - 7) >>" + Math.abs(Math.min(19, 4) - 7) );	
   
      System.out.println("Math.min(9, 5) >>" + Math.min(9, 5));
   
      System.out.println("Math.max(Math.round(6.4), Math.pow(2, 4)) >>" + Math.max(Math.round(6.4), Math.pow(2, 4)));
   
      System.out.println("Math.sqrt(25.0) >>" + Math.sqrt(25.0));
   
      System.out.println("Math.max(8, 10) +  >  + Math.min(2, 8) * 2 >>" + Math.max(8, 10) + " > " + Math.min(2, 8) * 2);
   
      System.out.println("Math.abs(-34) >>" + Math.abs(-34));	
   
      System.out.println("Math.abs(-4) * Math.sqrt(4.0) >>" + Math.abs(-4) * Math.sqrt(4.0));
   
   
   }
}